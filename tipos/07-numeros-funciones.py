import math  # modulo para funciones matematicas

print(round(1.3))  # muestra el tope mas cercano del numero decimal
print(abs(-100))  # valor absoluto
print(math.ceil(1.1))  # muestra el numero entero más proximo hacia arriba
print(math.floor(1.9))  # muestra el numero entero más proximo hacia abajo
# devuele bool si el dato ingresado es un numero natural
print(math.isnan(1))
print(math.pow(10, 3))  # potencia
print(math.sqrt(10))  # raiz cuadrada
